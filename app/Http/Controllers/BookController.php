     */
    public function store(Request $request)
    {
        $input = $request->all();

        $user = Auth::user();
        $input['user_id'] = $user->id;

        $book = Book::create($input);

        return back()
            ->with('success', 'رزرو شما با موفقیت ثبت شد!')
            ->with('book', $book);
    }

